def view(s:str, args='', reset=True):
    '''
    *–ü–æ–≤–µ—Ä—Ç–∞—î —Ä—è–¥–æ–∫ –∑ –∫–æ–ª—å–æ—Ä–æ–≤–∏–º –æ—Ñ–æ—Ä–º–ª–µ–Ω–Ω—è–º —ñ –µ—Ñ–µ–∫—Ç–∞–º–∏.*

    ### –ö–æ–º–±—ñ–Ω–æ–≤–∞–Ω–Ω—ñ –∑–Ω–∞—á–µ–Ω–Ω—è –¥–ª—è `args`
    * –ú–æ–∂–Ω–∞ –∫–æ–º–±—ñ–Ω—É–≤–∞—Ç–∏ –µ—Ñ–µ–∫—Ç`¬π`, –∫–æ–ª—ñ—Ä —à—Ä–∏—Ñ—Ç–∞`¬≤` —ñ —Ñ–æ–Ω—É`¬≥`.
    * –õ–∏—à–µ –ø–æ –æ–¥–Ω–æ–º—É –∑–Ω–∞—á–µ–Ω–Ω—é –¥–ª—è –∫–æ–∂–Ω–æ–≥–æ. –ù–∞–ø—Ä–∏–∫–ª–∞–¥: `*1d` –±—É–¥–µ —á–µ—Ä–≤–æ–Ω–∏–π –∂–∏—Ä–Ω–∏–π —à—Ä–∏—Ñ—Ç —ñ –∂–æ–≤—Ç–∏–π —Ñ–æ–Ω.
    * –§–ª–∞–≥ `reset` –æ–∑–Ω–∞—á–∞—î —â–æ –ø—ñ—Å–ª—è —Ü—å–æ–≥–æ —Ç–µ–∫—Å—Ç—É —Ñ–æ—Ä–º–∞—Ç—É–≤–∞–Ω–Ω—è –±—É–¥–µ —Å–∫–∏–Ω—É—Ç–µ –¥–æ –±–∞–∑–æ–≤–æ–≥–æ.

    **–¢–∞–±–ª–∏—Ü—è –ø—ñ–¥—Å—Ç–∞–Ω–æ–≤–∫–∏ –∫–æ–ª—å–æ—Ä—ñ–≤:**
    |       **–®—Ä–∏—Ñ—Ç**       |      **–§–æ–Ω**      |  **–ö–æ–ª—ñ—Ä**   |
    |:---------------------:|:-----------------:|:------------:|
    |          `0`          |        `a`        | *–ß–æ—Ä–Ω–∏–π*     |
    |          `1`          |        `b`        | *–ß–µ—Ä–≤–æ–Ω–∏–π*   |
    |          `2`          |        `c`        | *–ó–µ–ª–µ–Ω–∏–π*    |
    |          `3`          |        `d`        | *–ñ–æ–≤—Ç–∏–π*     |
    |          `4`          |        `e`        | *–°–∏–Ω—ñ–π*      |
    |          `5`          |        `f`        | *–§—ñ–æ–ª–µ—Ç–æ–≤–∏–π* |
    |          `6`          |        `g`        | *–ë—ñ—Ä—é–∑–æ–≤–∏–π*  |
    |          `7`          |        `h`        | *–ë—ñ–ª–∏–π*      |

    **–î–æ–¥–∞—Ç–∫–æ–≤—ñ –µ—Ñ–µ–∫—Ç–∏:**  
    |    **–°–∏–º–≤–æ–ª**      |    **–ï—Ñ–µ–∫—Ç**     |
    |:------------------:|:----------------:|
    |`*`<br>*(–∑—ñ—Ä–æ—á–∫–∞)*  | **–∂–∏—Ä–Ω–∏–π**       |
    |`-`<br>*(–¥–µ—Ñ—ñ—Å)*    | **–ø—ñ–¥–∫—Ä–µ—Å–ª–µ–Ω–∏–π** |
    |`'`<br>*(–∞–ø–æ—Å—Ç—Ä–æ—Ñ)* | **–Ω–µ–≤–∏–¥–∏–º–∏–π**    |
    '''

    # –°–∏–º–≤–æ–ª–∏ –¥–ª—è –ø—ñ–¥—Å—Ç–∞–Ω–æ–≤–∫–∏ –∫–æ–¥—ñ–≤
    repl = {
            '0': '30',
            '1': '31',
            '2': '32',
            '3': '33',
            '4': '34',
            '5': '35',
            '6': '36',
            '7': '37',
            'a': '40',
            'b': '41',
            'c': '42',
            'd': '43',
            'e': '44',
            'f': '45',
            'g': '46',
            'h': '47',
            '*': '1',
            '-': '4',
            "'": '8',
                }
    
    # –ó–∞–º—ñ–Ω—é—î–º–æ —Å–∏–º–≤–æ–ª–∏ –∑–∞ —Å–ª–æ–≤–Ω–∏–∫–æ–º —Ç–∞ –æ–±'—î–¥–Ω—É—î–º–æ —á–µ—Ä–µ–∑ ';'
    args = ";".join(repl[ch] for ch in args if ch in repl)
    # print("[" + args + "]", end='')

    # –Ø–∫—â–æ –∞—Ä–≥—É–º–µ–Ω—Ç–∏ —Ñ–æ—Ä–º–∞—Ç—É–≤–∞–Ω–Ω—è –ø—Ä–∏—Å—É—Ç–Ω—ñ, —Ç–æ —Ñ–æ—Ä–º–∞—Ç—É—î–º–æ
    result = ''
    if not args == '':
        result += '\033[' + args + 'm'

    result += s

    if reset == True:
        result += '\033[0m' # –°–∫–∏–¥–∞—î–º–æ —Ñ–æ—Ä–º–∞—Ç—É–≤–∞–Ω–Ω—è
    
    return result

